                    	<h5 style="color: #41d8d1;">Closing The Gap Between Technology & Business Transformations</h5>
                    	<p>As time goes by, traditional practitioners in areas such as Marketing, Finance, HR and Norakment have found more and more of their time is being taken up by projects with ever increasing digital elements. This has created a new tech literacy issue due to complicated technical language. To bridge the communication gap, the Diploma in Tech Fundamentals was created. This course will teach you how to speak the language of coders, without actually becoming a developer to do so.</p>
