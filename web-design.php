                        <p style="text-align: justify;">
                        Learning to design a website requires mastering many different skills from organizing the structure of the website, making sure it is easy to use and navigate, to designing the graphics and layout of the information. This web design course concentrates on the fundamentals of building a website. It starts with the basics, using HTML code to build a basic site, giving the learner a solid foundation in how a website is designed. It then moves on to using a html editor - Sublime Text which is a sophisticated text editor for code, markup and prose. Sublime Text is a proprietary cross-platform source code editor with a Python application programming interface (API). It natively supports many programming languages and markup languages. The final section of the web design course introduces the learner to MySql database and php programming.Students will also be introduced to Bootstrap which is a free front-end framework for faster and easier web development. Bootstrap includes HTML and CSS based design templates for typography, forms, buttons, tables, navigation, modals, image carousels and many other, as well as optional JavaScript plugins it also gives you the ability to easily create responsive designs</p>
